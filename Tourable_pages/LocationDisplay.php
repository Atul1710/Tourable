          The Sri Ranganathaswamy Temple or Thiruvarangam is a Hindu temple dedicated to Ranganatha, a form of the Supreme God, Maha Vishnu, located in Srirangam, Tiruchirapalli, Tamil Nadu, India.[4] Constructed in the Dravidian architectural style, the temple is glorified by Alvars in their Divya Prabhanda[5] and has the unique distinction of being not only the foremost among the 108 Divya Desams dedicated to Vishnu.[4], but also the largest functioning Hindu temple in the world.[6][7]

